package hub.sam.tef.reconciliation;

public class ReconciliationFailedException extends Exception {

	public ReconciliationFailedException() {
		super();
	}

	public ReconciliationFailedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ReconciliationFailedException(String arg0) {
		super(arg0);
	}

	public ReconciliationFailedException(Throwable arg0) {
		super(arg0);
	}

}
