package hub.sam.tef.tdl.delegators;

public class TDLException extends RuntimeException {

	public TDLException() {
		super();
	}

	public TDLException(String message, Throwable cause) {
		super(message, cause);
	}

	public TDLException(String message) {
		super(message);
	}

	public TDLException(Throwable cause) {
		super(cause);
	}

}
